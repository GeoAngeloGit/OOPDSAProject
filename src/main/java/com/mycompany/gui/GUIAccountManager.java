    public GUIAccountManager(User loginUser)
    {
        initComponents();
        loadAccountsTable(loginUser);

        accountsTbl.setFillsViewportHeight(true);
        accountsTbl.setRowHeight(24);

        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                                                        boolean isSelected, boolean hasFocus,
                                                        int row, int column) {
                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                setHorizontalAlignment(JLabel.CENTER);
                
                if (!isSelected) {
                    c.setBackground(row % 2 == 1 ? new Color(245,245, 245) : Color.WHITE);
                }

                return c;
            }
        };

        for (int i = 0; i < accountsTbl.getColumnCount(); i++) {
            accountsTbl.getColumnModel().getColumn(i).setCellRenderer(renderer);
        }

        accountsTbl.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseReleased(MouseEvent evt) {
                // stop any active cell editing first so value reads are consistent
                if (accountsTbl.isEditing()) {
                    accountsTbl.getCellEditor().stopCellEditing();
                }

                int viewRow = accountsTbl.getSelectedRow();
                int viewCol = accountsTbl.getSelectedColumn();

                // Status column only (convert view col to model index to be safe)
                if (viewRow == -1 || viewCol == -1) return;
                int modelCol = accountsTbl.convertColumnIndexToModel(viewCol);
                if (modelCol != 3) return;

                int modelRow = accountsTbl.convertRowIndexToModel(viewRow);

                String username = accountsTbl.getModel().getValueAt(modelRow, 0).toString();
                String currentStatus = accountsTbl.getModel().getValueAt(modelRow, 3).toString();

                if (username.equals(loginUser.getUsername())) {
                    JOptionPane.showMessageDialog(
                        GUIAccountManager.this,
                        "You cannot disable your own account.",
                        "Operation Not Allowed",
                        JOptionPane.WARNING_MESSAGE
                    );
                    return;
                }

                boolean activate = currentStatus.equalsIgnoreCase("DISABLED");

                int confirm = JOptionPane.showConfirmDialog(
                    GUIAccountManager.this,
                    "Change account status to " + (activate ? "ACTIVE" : "DISABLED") + "?",
                    "Confirm Status Change",
                    JOptionPane.YES_NO_OPTION
                );

                if (confirm == JOptionPane.YES_OPTION) {
                    try {
                        UserManager um = new UserManager();
                        um.loadUsers();
                        um.setUserStatus(username, activate);

                        loadAccountsTable(loginUser); // refresh table
                    } catch (IOException ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(
                            GUIAccountManager.this,
                            "Failed to update account status.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                        );
                    }
                }
            }
        });

        homeLbl.setFont(new Font("Verdana", Font.PLAIN, 12));
        homeLbl.setCursor(new Cursor(Cursor.HAND_CURSOR));
        homeLbl.setToolTipText("Go back Home");
        homeLbl.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent evt) {
                dispose();
            }
        });
    }
    private void loadAccountsTable(User loginUser) {
        DefaultTableModel model = (DefaultTableModel) accountsTbl.getModel();
        model.setRowCount(0);

        UserManager um = new UserManager();
        um.loadUsers();

        for (User u : um.getUsers()) {
            if (!u.getDepartment().equals(loginUser.getDepartment())) continue;
            if (u.getRole().equalsIgnoreCase("HEAD")) continue;

            model.addRow(new Object[] {
                u.getUsername(),
                u.getPassword(),
                u.getRole(),
                u.getStatus()
            });
        }
    }
    



